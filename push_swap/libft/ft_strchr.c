#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str != c && c <= 255)
	{
		if (*str == '\0')
			return (NULL);
		str++;
	}
	return (str);
}
